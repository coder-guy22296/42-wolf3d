#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int				sign;
	unsigned int	num;

	num = ft_pop_sign(n, &sign);
	if (sign)
		ft_putchar_fd('-', fd);
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
